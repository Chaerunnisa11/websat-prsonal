        <h1>Materi Operasi Himpunan</h1>
        <h2>Kurikulum Merdeka</h2>
        
        <h3>1. Gabungan (Union)</h3>
        <p>Gabungan dua himpunan <strong>A</strong> dan <strong>B</strong>, ditulis sebagai <strong>A ∪ B</strong>, adalah himpunan yang berisi semua elemen dari <strong>A</strong> dan <strong>B</strong>, tanpa elemen yang berulang.</p>
        <div class="example">
            <strong>Contoh:</strong> Jika A = {1, 2, 3} dan B = {3, 4, 5}, maka A ∪ B = {1, 2, 3, 4, 5}.
        </div>

        <h3>2. Irisan (Intersection)</h3>
        <p>Irisan dua himpunan <strong>A</strong> dan <strong>B</strong>, ditulis sebagai <strong>A ∩ B</strong>, adalah himpunan yang berisi elemen yang ada di <strong>A</strong> dan <strong>B</strong>.</p>
        <div class="example">
            <strong>Contoh:</strong> Jika A = {1, 2, 3} dan B = {3, 4, 5}, maka A ∩ B = {3}.
        </div>

        <h3>3. Selisih (Difference)</h3>
        <p>Selisih himpunan <strong>A</strong> terhadap <strong>B</strong>, ditulis sebagai <strong>A - B</strong>, adalah himpunan elemen yang ada di <strong>A</strong>, tetapi tidak di <strong>B</strong>.</p>
        <div class="example">
            <strong>Contoh:</strong> Jika A = {1, 2, 3} dan B = {3, 4, 5}, maka A - B = {1, 2}. Sebaliknya, B - A = {4, 5}.
        </div>

        <h3>4. Komplemen (Complement)</h3>
        <p>Komplemen suatu himpunan <strong>A</strong>, ditulis sebagai <strong>A<sup>c</sup></strong>, adalah himpunan semua elemen yang tidak ada di <strong>A</strong>, tetapi berada dalam semesta <strong>U</strong>.</p>
        <div class="example">
            <strong>Contoh:</strong> Jika U = {1, 2, 3, 4, 5} dan A = {1, 2, 3}, maka A<sup>c</sup> = {4, 5}.
        </div>
